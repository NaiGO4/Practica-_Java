public class Var {
    public static void main(String[] args) {
        var numeroEntero = 10;
        System.out.println("Numero entrero: " + numeroEntero);

        var numeroDouble = 10.0;
        System.out.println("Numero entrero: " + numeroDouble);

        var numeroFloat = 10.0f;
        System.out.println("Numero entrero: " + numeroFloat);
    }
}
